/*
    CodeSamplesTable Component.

    This is a Redux-managed React component that renders a table of code samples from
    GitHub Api. It displays repositories with metadata such as
    engagement metrics, last commit date, languages used, and categories.
 */
import React, { useState } from 'react'
import PropTypes from 'prop-types'
import { useSelector } from 'react-redux'

import { categoryIcon, categoryLabel, ContentCategories } from '../../components/categories/Component'
import Loading from '../../components/Loading'

import './styles.css'

// Used to validate the structure of the public/data/github.json data for each repository
// which itself is generated by the shared/githubDownloader.js script which is run
// weekly on a cron schedule by Github Actions and included in the react build so
// that it can be locally served to Redux.
const repoStateShape = PropTypes.shape({
  name: PropTypes.string.isRequired,
  html_url: PropTypes.string.isRequired,
  private: PropTypes.bool,
  description: PropTypes.string,
  visibility: PropTypes.string,
  forks: PropTypes.number,
  open_issues: PropTypes.number,
  watchers: PropTypes.number,
  stargazers_count: PropTypes.number,
  topics: PropTypes.arrayOf(PropTypes.string),
  categories: PropTypes.arrayOf(PropTypes.string),
  last_commit_date: PropTypes.string,
  total_commits: PropTypes.number,
  languages: PropTypes.arrayOf(
    PropTypes.shape({
      name: PropTypes.string.isRequired,
      bytes: PropTypes.number,
      percentage: PropTypes.string.isRequired,
    })
  ),
  readme: PropTypes.shape({
    content: PropTypes.string,
    name: PropTypes.string,
    path: PropTypes.string,
    sha: PropTypes.string,
    size: PropTypes.number,
    downloadUrl: PropTypes.string,
    readmeFormat: PropTypes.string,
  }),
  categoryLabels: PropTypes.arrayOf(PropTypes.string),
  categoryIcons: PropTypes.arrayOf(PropTypes.element),
})

// Used to validate the structure of the repositories state in Redux.
const repositoriesStateShape = PropTypes.shape({
  isLoading: PropTypes.bool.isRequired,
  errMess: PropTypes.string,
  repos: PropTypes.arrayOf(repoStateShape.isRequired),
})


// ---------------------------- Internal Components ------------------------------

